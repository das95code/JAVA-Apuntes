public class ClasesEnJava {
    
    /* Una clase se entiende como una estructura que sirve para reperesentar un determinado concepto o idea abstracta (una cosa).
       La definición de una clase sigue la forma básica:
       
       public/"default" class NombreDeLaClase extends ClaseMadre implements Interface1, Interface2...
    
       Solo se definen 2 niveles de encapsulación para la clase: public y "default". Esto se debe a que si restringimos la clase, no se podrán acceder a sus métodos aunque estos sean públicos.
       Otros modificadores que se pueden añadir son:
        
       ABSTRACT - Para indicar de que se trata de una clase abstracta. Es el punto inicial de la jerarquía de clases (es un supertipo), y no permite instanciación.
       FINAL - Indica que no puede heredarse de dicha clase. Es el punto final de la jerarquia de clases.
    
       Podemos definir dos o más clases en el mismo fichero .java, pero solo puede haber una clase PÚBLICA, que será la que dé nombre al fichero. No obstante, por cada clase encontrada se
       generará uun fichero que represente su .class.
    
       La clase está formada por MIEMBROS DE CLASE:
       
       ATRIBUTOS - define características de la clase (color, tamaño, edad, forma, etc.)
       CONSTRUCTORES - son las formas de crear instancias de clase en base a argumentos de entrada.
       MÉTODOS - lo que puede hacer dicha clase. Si devuelven algo, utilizan return.
    */
}
